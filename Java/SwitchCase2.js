let mês = "Dezembro"
switch (mês) {
    case "Março":
        console.log("O mês citado inicia o outono");
        break;
    case "Junho":
        console.log("O mês citado inicia o inverno");
        break;
    case "Setembro":
        console.log("O mês citado inicia a primavera");
        break;
    case "Dezembro":
        console.log("O mês citado inicia o verão");
        break;
    default:
        console.log("O nome citado não inicia nehuma estação");

}